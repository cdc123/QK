package org.lanqiao.entity;

import java.util.Date;

public class User {
    private int u_id;
    private String u_password;
    private String u_phone;
    private String u_power;
    private String u_name;
    private String u_sex;
    private String u_address;
    private Date u_birthday;
    private String u_introduce;
    private String u_image;

    public int getU_id() {
        return u_id;
    }

    public void setU_id(int u_id) {
        this.u_id = u_id;
    }

    public String getU_password() {
        return u_password;
    }

    public void setU_password(String u_password) {
        this.u_password = u_password;
    }

    public String getU_phone() {
        return u_phone;
    }

    public void setU_phone(String u_phone) {
        this.u_phone = u_phone;
    }

    public String getU_power() {
        return u_power;
    }

    public void setU_power(String u_power) {
        this.u_power = u_power;
    }

    public String getU_name() {
        return u_name;
    }

    public void setU_name(String u_name) {
        this.u_name = u_name;
    }

    public String getU_sex() {
        return u_sex;
    }

    public void setU_sex(String u_sex) {
        this.u_sex = u_sex;
    }

    public String getU_address() {
        return u_address;
    }

    public void setU_address(String u_address) {
        this.u_address = u_address;
    }

    public Date getU_birthday() {
        return u_birthday;
    }

    public void setU_birthday(Date u_birthday) {
        this.u_birthday = u_birthday;
    }

    public String getU_introduce() {
        return u_introduce;
    }

    public void setU_introduce(String u_introduce) {
        this.u_introduce = u_introduce;
    }

    public String getU_image() {
        return u_image;
    }

    public void setU_image(String u_image) {
        this.u_image = u_image;
    }



}
